"use client";
import { useState, useTransition } from "react";
import { AtcoderForm } from "./atcoder-form";
import { UserInfoRow } from "@/types/supabase";
import {
  Card,
  CardContent,
  CardDescription,
  CardHeader,
  CardTitle,
} from "@/components/ui/card";
import { Badge } from "@/components/ui/badge";
import { Button } from "@/components/ui/button";
import { Separator } from "@/components/ui/separator";
import { SubmissionGrass } from "./submission-grass";
import { SolvedProblemsList } from "./solved-problems";
import { DifficultyDistribution } from "./difficulty-distribution";
import { AvatarUpload } from "./avatar-upload";
import { TokenUsageCard } from "./token-usage";
import { RatingGraph } from "./rating-graph";
import { SolvedProblem, refreshAtcoderRating, getSolvedProblems } from "@/app/actions";
import { getRatingColor } from "@/lib/atcoder/rating-history";
import { RefreshCw, Pencil, Check, X } from "lucide-react";
import { Input } from "@/components/ui/input";
import { updatAtcoderHandle } from "@/app/actions";
import { useRouter } from "next/navigation";

export function ProfileForm({ rating, atcoder_handle }: UserInfoRow) {
    const [modify, setModify] = useState(false);

    if (rating === null || modify) {
        return (
      <Card className="w-full max-w-md">
        <CardHeader>
          <CardTitle>Atcoder 연동</CardTitle>
          <CardDescription>
            Atcoder 핸들을 입력하여 프로필을 연동하세요
          </CardDescription>
        </CardHeader>
        <CardContent>
          <AtcoderForm onSuccess={() => setModify(false)} />
        </CardContent>
      </Card>
        );
    }
    return (
    <Card className="w-full max-w-md">
      <CardHeader>
        <CardTitle>프로필 정보</CardTitle>
        <CardDescription>Atcoder 계정 정보를 확인하세요</CardDescription>
      </CardHeader>
      <CardContent className="space-y-4">
        <div className="space-y-2">
          <div className="flex items-center justify-between">
            <span className="text-sm font-medium text-muted-foreground">
              Atcoder Handle
            </span>
            <Badge variant="outline" className="text-sm">
              {atcoder_handle}
            </Badge>
          </div>
          <Separator />
          <div className="flex items-center justify-between">
            <span className="text-sm font-medium text-muted-foreground">
              Rating
            </span>
            <Badge variant="default" className="text-sm">
              {rating}
            </Badge>
          </div>
        </div>
        <Separator />
        <Button
          onClick={() => setModify(true)}
          variant="outline"
          className="w-full"
        >
          수정하기
        </Button>
      </CardContent>
    </Card>
    );
}

interface ProfileWithGrassProps extends UserInfoRow {
  solvedProblems?: SolvedProblem[];
}

export function ProfileWithGrass({ rating: initialRating, atcoder_handle, avatar_url: initialAvatarUrl, solvedProblems: initialSolvedProblems = [] }: ProfileWithGrassProps) {
  const [modify, setModify] = useState(false);
  const [editHandle, setEditHandle] = useState(atcoder_handle || "");
  const [isUpdating, setIsUpdating] = useState(false);
  const [rating, setRating] = useState(initialRating);
  const [avatarUrl, setAvatarUrl] = useState(initialAvatarUrl);
  const [solvedProblems, setSolvedProblems] = useState(initialSolvedProblems);
  const [isPending, startTransition] = useTransition();
  const [refreshKey, setRefreshKey] = useState(0);
  const router = useRouter();

  const handleRefresh = () => {
    if (!atcoder_handle) return;

    startTransition(async () => {
      // 레이팅 갱신 (히스토리도 함께 갱신됨)
      const newRating = await refreshAtcoderRating();
      if (newRating !== null) {
        setRating(newRating);
      }

      // 푼 문제 목록 갱신
      const newSolvedProblems = await getSolvedProblems(atcoder_handle);
      setSolvedProblems(newSolvedProblems);

      // RatingGraph 리렌더링 트리거
      setRefreshKey((prev) => prev + 1);
    });
  };

  const handleSaveHandle = async () => {
    if (!editHandle.trim()) return;

    setIsUpdating(true);
    try {
      const result = await updatAtcoderHandle(editHandle);
      if (!result.success) {
        alert("핸들 업데이트에 실패했습니다.");
        return;
      }

      // 레이팅 업데이트
      if (result.rating !== null) {
        setRating(result.rating);
      }

      // 푼 문제 목록 갱신
      if (result.handle) {
        const newSolvedProblems = await getSolvedProblems(result.handle);
        setSolvedProblems(newSolvedProblems);
      }

      // RatingGraph 리렌더링 트리거
      setRefreshKey((prev) => prev + 1);

      setModify(false);
      router.refresh();
    } catch (error) {
      console.error("Failed to update handle:", error);
      alert("핸들 업데이트에 실패했습니다.");
    } finally {
      setIsUpdating(false);
    }
  };

  const handleCancelEdit = () => {
    setEditHandle(atcoder_handle || "");
    setModify(false);
  };

  if (rating === null) {
    return (
      <>
        <div className="flex flex-col gap-2 self-start w-full">
          <h1 className="text-3xl font-bold tracking-tight">Profile</h1>
          <p className="text-muted-foreground">
            프로필 정보를 확인하고 관리하세요
          </p>
        </div>
        <Card className="w-full max-w-md">
          <CardHeader>
            <CardTitle>Atcoder 연동</CardTitle>
            <CardDescription>
              Atcoder 핸들을 입력하여 프로필을 연동하세요
            </CardDescription>
          </CardHeader>
          <CardContent>
            <AtcoderForm />
          </CardContent>
        </Card>
      </>
    );
  }

  return (
    <>
      <div className="flex items-center justify-between self-start w-full">
        <div className="flex flex-col gap-2">
          <h1 className="text-3xl font-bold tracking-tight">Profile</h1>
          <p className="text-muted-foreground">
            프로필 정보를 확인하고 관리하세요
          </p>
        </div>
        <Button
          variant="outline"
          size="sm"
          onClick={handleRefresh}
          disabled={isPending}
          className="gap-2"
        >
          <RefreshCw className={`h-4 w-4 ${isPending ? "animate-spin" : ""}`} />
          {isPending ? "갱신 중..." : "정보 갱신"}
        </Button>
      </div>
      <div className="w-full max-w-5xl space-y-6">
        <div className="flex flex-col md:flex-row gap-6">
          <Card
            className="w-full md:flex-1 border-2"
            style={{ borderColor: getRatingColor(rating ?? 0) }}
          >
            <CardHeader>
              <CardTitle>프로필 정보</CardTitle>
              <CardDescription>Atcoder 계정 정보를 확인하세요</CardDescription>
            </CardHeader>
            <CardContent className="space-y-4">
              <div className="flex items-start gap-6">
                <AvatarUpload
                  avatarUrl={avatarUrl}
                  onUpload={(url) => setAvatarUrl(url)}
                />
                <div className="flex-1 space-y-3">
                  <div className="flex items-center justify-between">
                    <span className="text-sm font-medium text-muted-foreground">
                      Atcoder Handle
                    </span>
                    {modify ? (
                      <div className="flex items-center gap-1">
                        <Input
                          value={editHandle}
                          onChange={(e) => setEditHandle(e.target.value)}
                          className="h-7 w-32 text-sm"
                          disabled={isUpdating}
                        />
                        <Button
                          variant="ghost"
                          size="icon"
                          className="h-7 w-7"
                          onClick={handleSaveHandle}
                          disabled={isUpdating}
                        >
                          <Check className="h-4 w-4 text-green-600" />
                        </Button>
                        <Button
                          variant="ghost"
                          size="icon"
                          className="h-7 w-7"
                          onClick={handleCancelEdit}
                          disabled={isUpdating}
                        >
                          <X className="h-4 w-4 text-red-600" />
                        </Button>
                      </div>
                    ) : (
                      <div className="flex items-center gap-2">
                        <Badge variant="outline" className="text-sm">
                          {atcoder_handle}
                        </Badge>
                        <Button
                          variant="ghost"
                          size="icon"
                          className="h-6 w-6"
                          onClick={() => setModify(true)}
                        >
                          <Pencil className="h-3 w-3" />
                        </Button>
                      </div>
                    )}
                  </div>
                  <Separator />
                  <div className="flex items-center justify-between">
                    <span className="text-sm font-medium text-muted-foreground">
                      Rating
                    </span>
                    <Badge
                      className="text-sm text-white"
                      style={{ backgroundColor: getRatingColor(rating ?? 0) }}
                    >
                      {rating}
                    </Badge>
                  </div>
                </div>
              </div>
            </CardContent>
          </Card>

          <div className="w-full md:flex-1">
            <TokenUsageCard />
          </div>
        </div>

        {atcoder_handle && <RatingGraph key={refreshKey} atcoderHandle={atcoder_handle} />}

      {atcoder_handle && (
        <Card className="w-full">
          <CardHeader>
            <CardTitle>AC Table</CardTitle>
            <CardDescription>
              당신의 기록을 확인하세요
            </CardDescription>
          </CardHeader>
          <CardContent>
            <SubmissionGrass userId={atcoder_handle} />
          </CardContent>
        </Card>
      )}

      {solvedProblems.length > 0 && (
        <>
          <DifficultyDistribution problems={solvedProblems} />
          <Card className="w-full">
            <CardHeader>
              <CardTitle>Solved Problems</CardTitle>
              <CardDescription>
                총 {solvedProblems.length}개의 문제를 풀었습니다
              </CardDescription>
            </CardHeader>
            <CardContent>
              <SolvedProblemsList problems={solvedProblems} />
            </CardContent>
          </Card>
        </>
      )}
      </div>
    </>
  );
}
